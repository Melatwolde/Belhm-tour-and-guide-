CREATE TABLE Booking(
    BookingID INT(6) PRIMARY KEY,
    Date DATE,
    Amount DECIMAL(8,3),
    BookingCID INT(6),
    book_packID INT(6),
    FOREIGN KEY (BookingCID) REFERENCES Client(Client_ID),
    FOREIGN KEY (book_packID) REFERENCES Tour_package(PackID)
);

